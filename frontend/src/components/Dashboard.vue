<script>
import DeviceCard from './DeviceCard.vue';
import axios from 'axios';

export default {
  name: 'MainDashboard',
  components: {
    DeviceCard,
  },
  data() {
    return {
      devices: [], // Массив устройств будет загружаться с бэка
    };
  },
  created() {
    this.fetchDevices();
  },
  methods: {
    async fetchDevices() {
      try {
        const response = await axios.get('http://0.0.0.0:8000/devices');
        this.devices = response.data; // Сохраняем полученные данные
      } catch (error) {
        console.error('Ошибка при загрузке устройств:', error);
      }
    },
    mapDevice(device) {
      // Преобразуем структуру данных для совместимости с DeviceCard
      console.log(device.id)
      return {
        id: device.id,
        name: device.name,
        uuid: device.uuid,
        html: device.html,
        description: device.description,
        protocolType: device.protocol.type,
        buttons: device.buttons,
        type: this.mapType(device.protocol.type), // Преобразуем тип протокола
        status: device.buttons.some(button => button.states.length > 0), // Проверяем состояние кнопок
        icon: this.getIcon(device.protocol.type), // Получаем иконку по типу устройства
        temperature: device.protocol.type === 'THERMOSTAT' ? 22 : null, // Добавляем температуру для термостатов
      };
    },
    mapType(protocolType) {
      switch (protocolType) {
        case 'RADIO_433MHz':
          return 'light'; // Для радиопротокола предполагаем лампу
        case 'THERMOSTAT':
          return 'thermostat';
        case 'CAMERA':
          return 'camera';
        default:
          return 'unknown';
      }
    },
    getIcon(protocolType) {
      switch (protocolType) {
        case 'RADIO_433MHz':
          return 'https://via.placeholder.com/50?text=lamp';
        case 'THERMOSTAT':
          return 'https://via.placeholder.com/50?text=thermo';
        case 'CAMERA':
          return 'https://via.placeholder.com/50?text=camera';
        default:
          return 'https://via.placeholder.com/50?text=unknown';
      }
    },
  },
};
